     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        if (Contact::fond($id)->delete()) {
            return redirect()->route("contact-list")->with("success", "status has been deleted !");
        }
        return redirect()->route("contact-list")->with("error", "Unable to delete status , please try agian  !");
    }

    public function statusManage($id)
    {
        $contact = Contact::find($id);
        $contact->status = !$contact->status;
        if ($contact->update()) {
            return redirect()->route("contact-list")->with("success", "status has been changed !");
        }
        return redirect()->route("contact-list")->with("Error", "Error Can not change the status , please try again !");
    }
